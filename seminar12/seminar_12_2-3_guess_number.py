import random as rnd
from sys import argv


__all__ = ['guess_the_number']


def guess_the_number(start: int, stop: int, attempts: int) -> bool:    
    num = rnd.randint(start, stop)
    print(f'–Ø –∑–∞–≥–∞–¥–∞–ª–∞ —á–∏—Å–ª–æ –æ—Ç {start} –¥–æ {stop}. –ß—Ç–æ–±—ã –µ–≥–æ –æ—Ç–≥–∞–¥–∞—Ç—å —É —Ç–µ–±—è –µ—Å—Ç—å {attempts} –ø–æ–ø—ã—Ç–æ–∫. –î–∞–≤–∞–π –Ω–∞—á–Ω–µ–º!')
    #print(f'–Ø –∑–∞–≥–∞–¥–∞–ª–∞ —á–∏—Å–ª–æ {num} –æ—Ç {start} –¥–æ {stop}. –ß—Ç–æ–±—ã –µ–≥–æ –æ—Ç–≥–∞–¥–∞—Ç—å —É —Ç–µ–±—è –µ—Å—Ç—å {attempts} –ø–æ–ø—ã—Ç–æ–∫. –î–∞–≤–∞–π –Ω–∞—á–Ω–µ–º!')
    for attempt in range(attempts):
        user_num = input(f'–ü–æ–ø—ã—Ç–∫–∞ ‚Ññ {attempt + 1}: ')        
        if int(user_num) < num:
            print('–±–æ–ª—å—à–µ')
        elif int(user_num) > num:
            print('–º–µ–Ω—å—à–µ')
        else:
            return True
    return False


